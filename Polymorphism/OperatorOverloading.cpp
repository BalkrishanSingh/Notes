// Following is the list of operators that can be overloaded.
// Unary Arithmetic operators (Negation -, Increment ++, Decrement --)
// Binary Arithmetic Operators(+,-,*,/,%)
// Assignment Operators (=,+=,-=,/=,*=,%=)
// Logical Operators (AND &&, OR ||, NOT !)
// Relation Operators (>,<,==,>=,<=)
// Bitwise operators (&,|,^,~)

// The operators that can't be overloaded are
// Scope Resolution Operator (::)
// Tenary Operator (conditon ? (Statement If true): (Statement if false))
// Sizeof Operator
// Pointer Operator
// Dot Operator
